const Income = require("../models/Income");
const Expense = require("../models/Expense");
const { isValidObjectId, Types } = require("mongoose");

//Dashboard data

exports.getDashboardData = async (req, res) => {
  try {
    const userId = req.user.id;
    const userObjectId = new Types.ObjectId(String(userId));

    // Fetch total incomes and expense
    const totalIncome = await Income.aggregate([
      { $match: { userId: userObjectId } },
      { $group: { _id: null, total: { $sum: "$amount" } } },
      {
        $project: {
          _id: 0,
          total: { $round: ["$total", 2] },
        },
      },
    ]);

    //console.log("totalIncome", {totalIncome, userId: isValidObjectId(userId)} )

    const totalExpense = await Expense.aggregate([
      { $match: { userId: userObjectId } },
      { $group: { _id: null, total: { $sum: "$amount" } } },
      {
        $project: {
          _id: 0,
          total: { $round: ["$total", 2] }, // âœ… round to 2 decimal places
        },
      },
    ]);

    console.log("totalExpense", {
      totalExpense,
      userId: isValidObjectId(userId),
    });


    //Get income transaction in the last 60 days
    const last60DaysIncomeTransactions = await Income.find({
      userId: userObjectId,
      date: { $gte: new Date(Date.now() - 60 * 24 * 60 * 60 * 1000) },
    }).sort({ date: -1 });


    // Get total income for last 60 days
    const incomeLast60Days = last60DaysIncomeTransactions.reduce(
      (sum, transaction) => sum + transaction.amount,
      0
    );
   

    //Get expense transactions in last 60 days
    const last60DaysExpenseTransactions = await Expense.find({
      userId: userObjectId,
      date: { $gte: new Date(Date.now() - 60 * 24 * 60 * 60 * 1000) },
    }).sort({ date: -1 });

    // Get total expenses for last 60 days
    const expensesLast60Days = last60DaysExpenseTransactions.reduce(
      (sum, transaction) => sum + transaction.amount,
      0
    );

    // Fetch last 5 transactions (income + expense)

    const lastTransactions = [
      ...(
        await Income.find({ userId: userObjectId }).sort({ date: -1 }).limit(5)
      ).map((txn) => ({
        ...txn.toObject(),
        type: "income",
      })),
      ...(
        await Expense.find({ userId: userObjectId }).sort({ date: -1 }).limit(5)
      ).map((txn) => ({
        ...txn.toObject(),
        type: "expense",
      })),
    ].sort((a, b) => b.date - a.date); // Sort latest first

    // Final Response
    res.json({
      //   totalBalance:
      //     (totalIncome[0]?.total || 0) - (totalExpense[0]?.total || 0),
      totalBalance:
        Math.round(
          ((totalIncome[0]?.total || 0) - (totalExpense[0]?.total || 0)) * 100
        ) / 100,

      totalIncome: totalIncome[0]?.total || 0,
      totalExpense: totalExpense[0]?.total || 0,
      last60DaysExpenses: {
        total: expensesLast60Days,
        transactions: last60DaysExpenseTransactions,
      },
      incomeLast60Days: {
        total: incomeLast60Days,
        transactions: last60DaysIncomeTransactions,
      },
      recentTransactions: lastTransactions,
    });
  } catch (error) {
    res.status(500).json({ message: "Server Error", error });
  }
};
